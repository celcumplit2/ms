import { readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import path from 'node:path';
import * as cheerio from 'cheerio';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const rootDir = path.resolve(__dirname, '..');

const sourceCssPath = path.join(rootDir, 'public', '_next', 'static', 'chunks', 'aa1c0878f930e7ab.css');
const outputCssPath = path.join(rootDir, 'public', '_next', 'static', 'chunks', 'home-aa1-pruned.css');
const homeHtmlPath = path.join(rootDir, 'public', 'index.html');

function findMatchingBrace(source, openIndex) {
  let depth = 0;

  for (let index = openIndex; index < source.length; index += 1) {
    const char = source[index];

    if (char === '{') {
      depth += 1;
    } else if (char === '}') {
      depth -= 1;

      if (depth === 0) {
        return index;
      }
    }
  }

  throw new Error(`Unmatched brace at index ${openIndex}`);
}

function splitSelectors(header) {
  const selectors = [];
  let start = 0;
  let depth = 0;

  for (let index = 0; index < header.length; index += 1) {
    const char = header[index];

    if (char === '(' || char === '[') {
      depth += 1;
    } else if (char === ')' || char === ']') {
      depth -= 1;
    } else if (char === ',' && depth === 0) {
      selectors.push(header.slice(start, index).trim());
      start = index + 1;
    }
  }

  selectors.push(header.slice(start).trim());
  return selectors.filter(Boolean);
}

function simplifySelector(selector) {
  return selector
    .replace(/:hover|:focus-visible|:focus|:active|:visited|:link|:target|:disabled|:enabled/g, '')
    .replace(/:not\(\s*:checked\s*\)/g, '')
    .replace(/:checked/g, '')
    .replace(/::before|::after|::placeholder/g, '')
    .replace(/\[disabled\]/g, '')
    .replace(/\s+/g, ' ')
    .trim();
}

function createSelectorMatcher(html) {
  const $ = cheerio.load(html);

  return function selectorMatches(selector) {
    const simplified = simplifySelector(selector);

    if (!simplified) {
      return false;
    }

    try {
      return $(simplified).length > 0;
    } catch {
      return false;
    }
  };
}

function filterCss(source, selectorMatches) {
  let index = 0;
  let output = '';

  while (index < source.length) {
    while (index < source.length && /\s/.test(source[index])) {
      index += 1;
    }

    if (index >= source.length) {
      break;
    }

    const nextBlock = source.indexOf('{', index);
    const nextTerminator = source.indexOf(';', index);

    if (nextBlock === -1 && nextTerminator === -1) {
      break;
    }

    if (nextTerminator !== -1 && (nextBlock === -1 || nextTerminator < nextBlock)) {
      const statement = source.slice(index, nextTerminator + 1).trim();

      if (statement) {
        output += statement;
      }

      index = nextTerminator + 1;
      continue;
    }

    const header = source.slice(index, nextBlock).trim();
    const endBlock = findMatchingBrace(source, nextBlock);
    const body = source.slice(nextBlock + 1, endBlock);

    if (header.startsWith('@media')) {
      const nested = filterCss(body, selectorMatches);

      if (nested) {
        output += `${header}{${nested}}`;
      }
    } else {
      const selectors = splitSelectors(header);

      if (selectors.some(selectorMatches)) {
        output += `${header}{${body}}`;
      }
    }

    index = endBlock + 1;
  }

  return output;
}

const sourceCss = readFileSync(sourceCssPath, 'utf8');
const homeHtml = readFileSync(homeHtmlPath, 'utf8');
const selectorMatches = createSelectorMatcher(homeHtml);
const filteredCss = filterCss(sourceCss, selectorMatches);

if (!filteredCss) {
  throw new Error('Pruned CSS is empty');
}

writeFileSync(
  outputCssPath,
  `/* Generated by scripts/prune-home-css.mjs */${filteredCss}`,
  'utf8',
);

const currentHomeHtml = readFileSync(homeHtmlPath, 'utf8');
const sourceHref = '/_next/static/chunks/aa1c0878f930e7ab.css';
const targetHref = '/_next/static/chunks/home-aa1-pruned.css';

if (currentHomeHtml.includes(sourceHref)) {
  writeFileSync(homeHtmlPath, currentHomeHtml.replace(sourceHref, targetHref), 'utf8');
} else if (!currentHomeHtml.includes(targetHref)) {
  throw new Error('Home page references neither the source CSS chunk nor the pruned CSS file');
}
